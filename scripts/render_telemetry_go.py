"""Pure, fail-closed renderer for the telemetry-v8 generated Go candidate.

The renderer consumes only an in-memory ``CandidateRenderIndex`` and its
compiler-owned ``GoAPIPlanIR``.  It does not read registry YAML, reviewed
goldens, existing Go sources, or prior generated bytes.  This first foundation
renders the exact constant declaration inventory and the seven-file package
skeleton.  Compiler facts that are not yet syntax-complete are rejected before
any output object is constructed; incomplete family, structured, producer, or
fixture wrappers are never substituted with placeholders.

The returned objects are the exact input types accepted by
``telemetry_go_output_coordinator.preflight_go_outputs``.
"""

