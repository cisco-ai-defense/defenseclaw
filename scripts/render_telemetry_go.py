class GoRenderError(RuntimeError):
    """The in-memory compiler contract cannot produce a complete candidate."""


@dataclasses.dataclass(frozen=True, slots=True)
class GoRenderCandidate:
    """One complete seven-file candidate plus its coordinator inventory."""

    outputs: tuple[RenderedGoOutput, ...]
    declaration_inventory: tuple[GoFileDeclarationInventory, ...]
    expected_declaration_keys: tuple[GoDeclarationKey, ...]
    materialized_view_sha256: str
    candidate_render_index_sha256: str
    go_symbol_table_sha256: str


_SHA256: Final = re.compile(r"^[0-9a-f]{64}$")
_GO_IDENTIFIER: Final = re.compile(r"^[A-Za-z][A-Za-z0-9]*$")
_EXPECTED_HEADERS: Final = (
    "materialized_view_sha256",
    "candidate_render_index_sha256",
    "go_symbol_table_sha256",
)
_MAX_DECLARATIONS: Final = 10_000
_MAX_PRIVATE_ROWS: Final = 100_000
_MAX_GO_STRING_BYTES: Final = 1_048_576
_FAMILY_SIGNALS: Final = frozenset({"log", "span", "metric"})
_FAMILY_SIGNAL_SYMBOLS: Final = {"log": "SignalLogs", "span": "SignalTraces", "metric": "SignalMetrics"}
_SINGLE_LINE_IF: Final = re.compile(r"^(?P<indent>\t*)if (?P<condition>.+?) \{ (?P<body>.+) \}$")
_KEYED_LITERAL_LINE: Final = re.compile(r"^(?P<indent>\t+)(?P<key>[A-Za-z][A-Za-z0-9]*): +(?P<value>\S.*),$")
_STRUCT_FIELD_LINE: Final = re.compile(r"^(?P<indent>\t)(?P<name>[A-Za-z][A-Za-z0-9]*) (?P<type>.+)$")
_CONST_LINE: Final = re.compile(r"^(?P<indent>\t)(?P<name>[A-Za-z][A-Za-z0-9]*) (?P<type>[^ ]+) = (?P<value>.+)$")


def _read(value: Any, name: str, path: str) -> Any:
    if dataclasses.is_dataclass(value) or hasattr(value, name):
        try:
            return getattr(value, name)
        except AttributeError:
            pass
    if isinstance(value, Mapping) and name in value:
        return value[name]
    raise GoRenderError(f"{path}: missing compiler fact {name}")


def _sequence(value: Any, path: str, *, maximum: int) -> tuple[Any, ...]:
    if not isinstance(value, Sequence) or isinstance(value, (str, bytes, bytearray)):
        raise GoRenderError(f"{path}: expected a bounded sequence")
    if len(value) > maximum:
        raise GoRenderError(f"{path}: sequence exceeds the renderer bound of {maximum}")
    return tuple(value)


def _string(value: Any, path: str) -> str:
    if not isinstance(value, str) or not value:
        raise GoRenderError(f"{path}: expected a nonempty string")
    return value


def _digest(value: Any, path: str) -> str:
    value = _string(value, path)
    if _SHA256.fullmatch(value) is None:
        raise GoRenderError(f"{path}: expected a canonical SHA-256 digest")
    return value


def _signed_int64(value: Any, path: str) -> int:
    if type(value) is not int or value < -(1 << 63) or value > (1 << 63) - 1:
        raise GoRenderError(f"{path}: expected a signed 64-bit integer")
    return value


def _require_typed_root(value: Any, expected_name: str, path: str) -> None:
    if not dataclasses.is_dataclass(value) or isinstance(value, type) or type(value).__name__ != expected_name:
        raise GoRenderError(f"{path}: expected canonical typed {expected_name}")


def _verify_compiler_digests(index: Any, plan: Any) -> tuple[str, str]:
    _require_typed_root(index, "CandidateRenderIndex", "CandidateRenderIndex")
    _require_typed_root(plan, "GoAPIPlanIR", "GoAPIPlanIR")
    recorded_api = _digest(_read(plan, "api_plan_sha256", "GoAPIPlanIR"), "GoAPIPlanIR.api_plan_sha256")
    try:
        api_valid = plan.verify_digest()
    except (TypeError, ValueError, RuntimeError) as exc:
        raise GoRenderError("GoAPIPlanIR digest verification failed") from exc
    if api_valid is not True:
        raise GoRenderError("GoAPIPlanIR.api_plan_sha256 disagrees with its typed facts")
    if (
        _digest(_read(index, "api_plan_sha256", "CandidateRenderIndex"), "CandidateRenderIndex.api_plan_sha256")
        != recorded_api
    ):
        raise GoRenderError("CandidateRenderIndex and GoAPIPlanIR API-plan digests disagree")
    recorded_candidate = _digest(
        _read(index, "candidate_render_index_sha256", "CandidateRenderIndex"),
        "CandidateRenderIndex.candidate_render_index_sha256",
    )
    try:
        candidate_valid = index.verify_digest()
    except (TypeError, ValueError, RuntimeError) as exc:
        raise GoRenderError("CandidateRenderIndex digest verification failed") from exc
    if candidate_valid is not True:
        raise GoRenderError("CandidateRenderIndex digest disagrees with its typed facts")
    return recorded_api, recorded_candidate


def _identifier(value: Any, path: str) -> str:
    value = _string(value, path)
    if _GO_IDENTIFIER.fullmatch(value) is None:
        raise GoRenderError(f"{path}: compiler supplied an invalid Go identifier")
    return value


def _go_string(value: Any, path: str) -> str:
    if not isinstance(value, str):
        raise GoRenderError(f"{path}: expected a string")
    if any(0xD800 <= ord(character) <= 0xDFFF for character in value):
        raise GoRenderError(f"{path}: Go string literal contains an unpaired surrogate")
    if len(value.encode("utf-8")) > _MAX_GO_STRING_BYTES:
        raise GoRenderError(f"{path}: Go string literal exceeds the renderer bound")
    # JSON and Go interpreted string literals share the escapes used here.  Keep
    # Unicode source text intact; there is no semantic normalization in rendering.
    return json.dumps(value, ensure_ascii=False)


def _type_name(type_ref: Any, path: str) -> str:
    arm = _string(_read(type_ref, "arm", path), f"{path}.arm")
    name = _read(type_ref, "name", path)
    element = _read(type_ref, "element", path)
    if arm in {"builtin", "named"}:
        if element is not None:
            raise GoRenderError(f"{path}: scalar Go type unexpectedly has an element")
        return _identifier(name, f"{path}.name")
    if arm in {"optional", "slice"}:
        if name is not None or element is None:
            raise GoRenderError(f"{path}: container Go type requires only an element")
        nested = _type_name(element, f"{path}.element")
        return f"Optional[{nested}]" if arm == "optional" else f"[]{nested}"
    raise GoRenderError(f"{path}: unsupported Go type AST arm {arm}")


def _producer_type_name(type_ref: Any, path: str) -> str:
    arm = _string(_read(type_ref, "arm", path), f"{path}.arm")
    name = _read(type_ref, "name", path)
    element = _read(type_ref, "element", path)
    if arm in {"builtin", "named"}:
        if element is not None:
            raise GoRenderError(f"{path}: scalar producer type unexpectedly has an element")
        return _identifier(name, f"{path}.name")
    if arm in {"slice", "pointer"}:
        if name is not None or element is None:
            raise GoRenderError(f"{path}: producer container type is incomplete")
        prefix = "[]" if arm == "slice" else "*"
        return prefix + _producer_type_name(element, f"{path}.element")
    if arm == "array":
        length = _read(type_ref, "length", path)
        if name is not None or element is None or type(length) is not int or length < 0:
            raise GoRenderError(f"{path}: producer array type is incomplete")
        return f"[{length}]" + _producer_type_name(element, f"{path}.element")
    if arm == "map":
        key = _read(type_ref, "key", path)
        if name is not None or element is None or key is None:
            raise GoRenderError(f"{path}: producer map type is incomplete")
        return "map[" + _producer_type_name(key, f"{path}.key") + "]" + _producer_type_name(element, f"{path}.element")
    raise GoRenderError(f"{path}: unsupported producer Go type AST arm {arm}")


def _fixture_type_name(type_ref: Any, path: str) -> str:
    arm = _string(_read(type_ref, "arm", path), f"{path}.arm")
    name = _read(type_ref, "name", path)
    element = _read(type_ref, "element", path)
    if arm in {"builtin", "named"}:
        if element is not None:
            raise GoRenderError(f"{path}: scalar fixture type unexpectedly has an element")
        return _identifier(name, f"{path}.name")
    if arm == "qualified":
        if not isinstance(name, str) or re.fullmatch(r"[A-Za-z][A-Za-z0-9]*\.[A-Za-z][A-Za-z0-9]*", name) is None:
            raise GoRenderError(f"{path}: invalid qualified fixture type")
        return name
    if arm in {"optional", "slice", "pointer"}:
        if name is not None or element is None:
            raise GoRenderError(f"{path}: fixture container type is incomplete")
        nested = _fixture_type_name(element, f"{path}.element")
        return {"optional": f"Optional[{nested}]", "slice": f"[]{nested}", "pointer": f"*{nested}"}[arm]
    raise GoRenderError(f"{path}: unsupported fixture Go type AST arm {arm}")


def _bool(value: Any, path: str) -> str:
    if type(value) is not bool:
        raise GoRenderError(f"{path}: expected Boolean")
    return "true" if value else "false"


def _number(value: Any, path: str) -> str:
    if isinstance(value, bool) or not isinstance(value, (int, float)):
        raise GoRenderError(f"{path}: expected a finite number")
    if isinstance(value, float):
        if value != value or value in {float("inf"), float("-inf")}:
            raise GoRenderError(f"{path}: expected a finite number")
        rendered = repr(value)
        return rendered if "." in rendered or "e" in rendered.lower() else rendered + ".0"
    return str(value)


def _typed_symbol(value: Any, path: str) -> str:
    symbol = _identifier(_read(value, "symbol", path), f"{path}.symbol")
    conversion = _read(value, "conversion_type", path)
    if conversion is None:
        return symbol
    return f"{_type_name(conversion, f'{path}.conversion_type')}({symbol})"


def _imports(items: Sequence[tuple[str | None, str]], path: str) -> list[str]:
    if not items:
        return []
    lines = ["import ("]
    for alias, import_path in items:
        literal = _go_string(import_path, path)
        lines.append(f"\t{alias} {literal}" if alias is not None else f"\t{literal}")
    lines.extend((")", ""))
    return lines


def _align_keyed_literal_runs(lines: Sequence[str]) -> list[str]:
    result = list(lines)
    position = 0
    while position < len(result):
        match = _KEYED_LITERAL_LINE.fullmatch(result[position])
        if match is None:
            position += 1
            continue
        end = position + 1
        matches = [match]
        while end < len(result):
            candidate = _KEYED_LITERAL_LINE.fullmatch(result[end])
            if candidate is None or candidate.group("indent") != match.group("indent"):
                break
            matches.append(candidate)
            end += 1
        maximum = max(len(item.group("key")) for item in matches)
        for offset, item in enumerate(matches):
            padding = " " * (maximum - len(item.group("key")) + 1)
            result[position + offset] = (
                item.group("indent") + item.group("key") + ":" + padding + item.group("value") + ","
            )
        position = end
    return result


def _align_decl_block(lines: list[str], start: int, end: int, pattern: re.Pattern[str]) -> None:
    matches = [pattern.fullmatch(lines[position]) for position in range(start, end)]
    selected = [match for match in matches if match is not None]
    if len(selected) != end - start or not selected:
        return
    maximum_name = max(len(match.group("name")) for match in selected)
    if pattern is _CONST_LINE:
        maximum_type = max(len(match.group("type")) for match in selected)
        for position, match in zip(range(start, end), selected, strict=True):
            lines[position] = (
                match.group("indent")
                + match.group("name").ljust(maximum_name)
                + " "
                + match.group("type").ljust(maximum_type)
                + " = "
                + match.group("value")
            )
        return
    for position, match in zip(range(start, end), selected, strict=True):
        lines[position] = match.group("indent") + match.group("name").ljust(maximum_name) + " " + match.group("type")


def _go_style_lines(raw_lines: Sequence[str]) -> list[str]:
    """Apply only the closed formatting rules used by renderer-owned templates."""

    expanded: list[str] = []
    for line in raw_lines:
        match = _SINGLE_LINE_IF.fullmatch(line)
        if match is None:
            expanded.append(line)
            continue
        indent = match.group("indent")
        expanded.extend(
            (
                indent + "if " + match.group("condition") + " {",
                indent + "\t" + match.group("body"),
                indent + "}",
            )
        )

    position = 0
    while position < len(expanded):
        if expanded[position] == "const (":
            end = position + 1
            while end < len(expanded) and expanded[end] != ")":
                end += 1
            _align_decl_block(expanded, position + 1, end, _CONST_LINE)
            position = end + 1
            continue
        if re.fullmatch(r"type [A-Za-z][A-Za-z0-9]* struct \{", expanded[position]):
            end = position + 1
            while end < len(expanded) and expanded[end] != "}":
                end += 1
            _align_decl_block(expanded, position + 1, end, _STRUCT_FIELD_LINE)
            position = end + 1
            continue
        position += 1
    return _align_keyed_literal_runs(expanded)


def _go_source(lines: Sequence[str]) -> bytes:
    return ("\n".join(_go_style_lines(lines)).rstrip() + "\n").encode("utf-8")


def _struct_lines(symbol: str, fields: Sequence[Any], path: str) -> list[str]:
    lines = [f"type {_identifier(symbol, path)} struct {{"]
    ordered = sorted(fields, key=lambda item: _read(item, "order", path) if hasattr(item, "order") else 0)
    for position, field in enumerate(ordered):
        selector = _identifier(_read(field, "selector", f"{path}[{position}]"), f"{path}[{position}].selector")
        lines.append(f"\t{selector} {_type_name(_read(field, 'type_ref', path), path)}")
    lines.extend(("}", ""))
    return lines


def _constant_line(declaration: Any, position: int) -> str:
    path = f"GoAPIPlanIR.declarations[{position}]"
    if _read(declaration, "declaration_form", path) != "exported_const":
        raise GoRenderError(f"{path}: IDs file contains a non-constant declaration")
    symbol = _identifier(_read(declaration, "symbol", path), f"{path}.symbol")
    literal_kind = _read(declaration, "literal_kind", path)
    literal_value = _read(declaration, "literal_value", path)
    type_ref = _read(declaration, "go_type", path)
    if type_ref is None:
        raise GoRenderError(f"{path}: exported constant has no exact Go type")
    go_type = _type_name(type_ref, f"{path}.go_type")
    if literal_kind == "string":
        if go_type != "string" or not isinstance(literal_value, str):
            raise GoRenderError(f"{path}: string constant type/value disagree")
        literal = _go_string(literal_value, f"{path}.literal_value")
    elif literal_kind == "integer":
        if go_type != "int" or isinstance(literal_value, bool) or not isinstance(literal_value, int):
            raise GoRenderError(f"{path}: integer constant type/value disagree")
        # Preserve the compiler-selected exact Go type.  Keep generated
        # constants portable to every Go target rather than accepting a value
        # that compiles only where int happens to be 64 bits.
        if literal_value < -(2**31) or literal_value >= 2**31:
            raise GoRenderError(f"{path}: integer constant exceeds portable Go int")
        literal = str(literal_value)
    else:
        raise GoRenderError(f"{path}: unsupported exact constant literal kind")
    return f"\t{symbol} {go_type} = {literal}"


def _render_ids_body(declarations: tuple[Any, ...]) -> bytes:
    lines = ["package observability", ""]
    if declarations:
        lines.extend(
            ("const (", *(_constant_line(item, position) for position, item in enumerate(declarations)), ")", "")
        )
    return _go_source(lines)


def _limits_literal(value: Any, path: str) -> str:
    fields = (
        ("maxEncodedBytes", "max_encoded_bytes"),
        ("maxItemUTF8Bytes", "max_item_utf8_bytes"),
        ("maxItems", "max_items"),
        ("maxDepth", "max_depth"),
        ("maxProperties", "max_properties"),
    )
    pieces = [f"{target}: {_read(value, source, path)}" for target, source in fields]
    if any(type(_read(value, source, path)) is not int or _read(value, source, path) < 0 for _, source in fields):
        raise GoRenderError(f"{path}: invalid structured limits")
    return "familyStructuredLimits{" + ", ".join(pieces) + "}"


def _constraints_literal(value: Any, path: str) -> str:
    pieces: list[str] = []
    scalar_fields = (
        ("maxUTF8Bytes", "max_utf8_bytes"),
        ("minItems", "min_items"),
        ("maxItems", "max_items"),
    )
    for target, source in scalar_fields:
        item = _read(value, source, path)
        if type(item) is not int or item < 0:
            raise GoRenderError(f"{path}.{source}: invalid constraint")
        if item:
            pieces.append(f"{target}: {item}")
    pattern = _read(value, "pattern", path)
    if pattern:
        pieces.append(f"pattern: {_go_string(pattern, f'{path}.pattern')}")
    enum = _sequence(_read(value, "enum_values", path), f"{path}.enum_values", maximum=4096)
    if enum:
        pieces.append("enum: []string{" + ", ".join(_go_string(item, f"{path}.enum") for item in enum) + "}")
    for stem, go_name in (("int", "Int"), ("uint", "Uint"), ("float", "Float")):
        for edge, title in (("min", "Min"), ("max", "Max")):
            item = _read(value, f"{stem}_{edge}", path)
            if item is not None:
                pieces.append(f"has{go_name}{title}: true")
                pieces.append(f"{stem}{title}: {_number(item, f'{path}.{stem}_{edge}')}")
    structured = _read(value, "structured", path)
    if structured is not None:
        pieces.append(f"structured: {_limits_literal(structured, f'{path}.structured')}")
    return "familyFieldConstraints{" + ", ".join(pieces) + "}"


def _field_descriptor_literal(value: Any, path: str) -> str:
    pieces = [
        f"key: {_go_string(_read(value, 'key', path), f'{path}.key')}",
        f"typeOf: {_typed_symbol(_read(value, 'type_ref', path), f'{path}.type_ref')}",
        f"requirement: {_typed_symbol(_read(value, 'requirement_ref', path), f'{path}.requirement_ref')}",
    ]
    condition = _read(value, "condition_id", path)
    if condition is not None:
        pieces.append(f"conditionID: {_go_string(condition, f'{path}.condition_id')}")
    false_requirement = _read(value, "false_requirement_ref", path)
    if false_requirement is not None:
        pieces.append(f"falseRequirement: {_typed_symbol(false_requirement, f'{path}.false_requirement_ref')}")
    pieces.extend(
        (
            f"fieldClass: {_typed_symbol(_read(value, 'field_class_ref', path), f'{path}.field_class_ref')}",
            f"constraints: {_constraints_literal(_read(value, 'typed_constraints', path), f'{path}.constraints')}",
            f"source: {_typed_symbol(_read(value, 'source_ref', path), f'{path}.source_ref')}",
        )
    )
    return "familyFieldDescriptor{" + ", ".join(pieces) + "}"


def _field_descriptor_slice(values: Any, path: str) -> str:
    items = _sequence(values, path, maximum=_MAX_PRIVATE_ROWS)
    return (
        "[]familyFieldDescriptor{"
        + ", ".join(_field_descriptor_literal(item, f"{path}[{position}]") for position, item in enumerate(items))
        + "}"
    )


def _base_contract_literal(value: Any, path: str) -> str:
    identity = _read(value, "identity", path)
    outcome = _read(value, "outcome", path)
    allowed = _sequence(_read(outcome, "allowed", path), f"{path}.outcome.allowed", maximum=128)
    relations: list[str] = []
    for position, relation in enumerate(
        _sequence(_read(value, "cross_field_relations", path), f"{path}.cross_field_relations", maximum=64)
    ):
        relation_path = f"{path}.cross_field_relations[{position}]"
        if _read(relation, "arm", relation_path) != "string-int64-bijection":
            raise GoRenderError(f"{relation_path}: unsupported cross-field relation arm")
        entries = _sequence(
            _read(relation, "entries", relation_path),
            f"{relation_path}.entries",
            maximum=MAX_CROSS_FIELD_RELATION_ENTRIES,
        )
        entry_literals = ", ".join(
            "{value: "
            + _go_string(_read(entry, "value", relation_path), f"{relation_path}.entries[{entry_at}].value")
            + f", code: {_signed_int64(_read(entry, 'code', relation_path), f'{relation_path}.entries[{entry_at}].code')}}}"
            for entry_at, entry in enumerate(entries)
        )
        relations.append(
            "{"
            + ", ".join(
                (
                    f"valueKey: {_go_string(_read(relation, 'value_key', relation_path), relation_path)}",
                    f"codeKey: {_go_string(_read(relation, 'code_key', relation_path), relation_path)}",
                    f"entries: []familyValueCodeEntry{{{entry_literals}}}",
                    f"mismatchCode: {_typed_symbol(_read(relation, 'mismatch_error', relation_path), relation_path)}",
                )
            )
            + "}"
        )
    return (
        "familyDescriptorContract{"
        + ", ".join(
            (
                f"id: {_go_string(_read(value, 'family_id', path), f'{path}.family_id')}",
                "identity: EventIdentity{"
                + ", ".join(
                    (
                        f"Bucket: {_typed_symbol(_read(identity, 'bucket', path), f'{path}.bucket')}",
                        f"Signal: {_typed_symbol(_read(identity, 'signal', path), f'{path}.signal')}",
                        f"Name: {_typed_symbol(_read(identity, 'event_name', path), f'{path}.event_name')}",
                    )
                )
                + "}",
                f"familySchemaVersion: {_read(value, 'family_schema_version', path)}",
                "outcome: familyOutcomePolicy{"
                + f"requirement: {_typed_symbol(_read(outcome, 'requirement', path), f'{path}.outcome.requirement')}, "
                + "allowed: []Outcome{"
                + ", ".join(_typed_symbol(item, f"{path}.outcome.allowed") for item in allowed)
                + "}}",
                f"fields: {_field_descriptor_slice(_read(value, 'fields', path), f'{path}.fields')}",
                "crossFieldRelations: []familyCrossFieldRelation{" + ", ".join(relations) + "}",
            )
        )
        + "}"
    )


def _span_name_literal(parts: Any, path: str) -> str:
    result: list[str] = []
    for position, part in enumerate(_sequence(parts, path, maximum=128)):
        arm = _read(part, "arm", f"{path}[{position}]")
        if arm == "literal":
            literal = _read(part, "literal", path)
            if _read(part, "field_key", path) is not None:
                raise GoRenderError(f"{path}[{position}]: literal span-name arm carries a field")
            result.append(f"{{literal: {_go_string(literal, path)}}}")
        elif arm == "field":
            field = _read(part, "field_key", path)
            if _read(part, "literal", path) is not None:
                raise GoRenderError(f"{path}[{position}]: field span-name arm carries a literal")
            result.append(f"{{field: {_go_string(field, path)}}}")
        else:
            raise GoRenderError(f"{path}[{position}]: unknown span-name arm")
    return "[]spanNamePart{" + ", ".join(result) + "}"


def _render_compact_catalog_body(
    descriptors: Sequence[Any], identity_rows: Sequence[tuple[str, str, str, str, str]]
) -> bytes:
    """Store immutable contracts once behind sealed per-family capabilities."""

    def compact_base(value: Any, path: str, *, elide_outer: bool) -> str:
        literal = _base_contract_literal(value, path)
        if elide_outer:
            literal = literal.removeprefix("familyDescriptorContract")
        for redundant in ("familyFieldDescriptor", "familyCrossFieldRelation", "familyValueCodeEntry"):
            literal = re.sub(r"(?<!\[\])" + redundant + r"\{", "{", literal)
        return literal

    def compact_fields(value: Any, path: str) -> str:
        return re.sub(r"(?<!\[\])familyFieldDescriptor\{", "{", _field_descriptor_slice(value, path))

    lines = [
        "package observability",
        "",
        "var generatedFamilyBaseContracts = map[string]familyDescriptorContract{",
    ]
    base_key_width = max(len(_go_string(_read(item, "family_id", "descriptor"), "descriptor")) for item in descriptors)
    for position, descriptor in enumerate(descriptors):
        path = f"GoAPIPlanIR.descriptors[{position}]"
        family_id = _string(_read(descriptor, "family_id", path), f"{path}.family_id")
        base = _read(_read(descriptor, "catalog_contract", path), "base", path)
        key = _go_string(family_id, path)
        lines.append(
            f"\t{key}: "
            + " " * (base_key_width - len(key))
            + compact_base(base, f"{path}.base", elide_outer=True)
            + ","
        )
    lines.extend(
        (
            "}",
            "",
            "var generatedFamilyMetricContracts = map[string]familyMetricContract{",
        )
    )
    metric_descriptors = [
        item
        for item in descriptors
        if _read(_read(item, "catalog_contract", "descriptor"), "metric", "descriptor") is not None
    ]
    metric_key_width = max(
        len(_go_string(_read(item, "family_id", "descriptor"), "descriptor")) for item in metric_descriptors
    )
    for position, descriptor in enumerate(descriptors):
        path = f"GoAPIPlanIR.descriptors[{position}]"
        family_id = _string(_read(descriptor, "family_id", path), f"{path}.family_id")
        catalog = _read(descriptor, "catalog_contract", path)
        metric = _read(catalog, "metric", path)
        if metric is None:
            continue
        base = _read(catalog, "base", path)
        key = _go_string(family_id, path)
        lines.append(
            f"\t{key}: "
            + " " * (metric_key_width - len(key))
            + f"{{familyDescriptorContract: {compact_base(base, f'{path}.base', elide_outer=False)}, "
            f"valueType: {_typed_symbol(_read(metric, 'value_type', path), path)}, "
            f"attributeLimits: {_limits_literal(_read(metric, 'attribute_limits', path), path)}, "
            f"instrumentName: {_typed_symbol(_read(metric, 'instrument_name', path), path)}, "
            f"instrumentType: {_go_string(_read(metric, 'instrument_type', path), path)}, "
            f"unit: {_go_string(_read(metric, 'unit', path), path)}, "
            f"temporality: {_go_string(_read(metric, 'temporality', path), path)}}},"
        )
    lines.extend(
        (
            "}",
            "",
            "var generatedFamilyTraceContracts = map[string]familyTraceContract{",
        )
    )
    event_rows: list[tuple[str, str]] = []
    trace_descriptors = [
        item
        for item in descriptors
        if _read(_read(item, "catalog_contract", "descriptor"), "trace", "descriptor") is not None
    ]
    trace_key_width = max(
        len(_go_string(_read(item, "family_id", "descriptor"), "descriptor")) for item in trace_descriptors
    )
    for position, descriptor in enumerate(descriptors):
        path = f"GoAPIPlanIR.descriptors[{position}]"
        family_id = _string(_read(descriptor, "family_id", path), f"{path}.family_id")
        catalog = _read(descriptor, "catalog_contract", path)
        trace = _read(catalog, "trace", path)
        if trace is None:
            continue
        base = _read(catalog, "base", path)
        events = _sequence(_read(trace, "allowed_events", path), f"{path}.events", maximum=1024)
        links = _sequence(_read(trace, "allowed_links", path), f"{path}.links", maximum=1024)
        event_literals = ", ".join(
            f"{_identifier(_read(event, 'private_helper_symbol', path), path)}()" for event in events
        )
        allowed_kinds = ", ".join(
            _go_string(item, path) for item in _sequence(_read(trace, "allowed_kinds", path), path, maximum=64)
        )
        key = _go_string(family_id, path)
        lines.append(
            f"\t{key}: "
            + " " * (trace_key_width - len(key))
            + f"{{familyDescriptorContract: {compact_base(base, f'{path}.base', elide_outer=False)}, "
            f"allowedKinds: []string{{{allowed_kinds}}}, spanName: {_span_name_literal(_read(trace, 'span_name', path), path)}, "
            f"attributeLimits: {_limits_literal(_read(trace, 'attribute_limits', path), path)}, resourceFields: {compact_fields(_read(trace, 'resource_fields', path), path)}, "
            f"resourceLimits: {_limits_literal(_read(trace, 'resource_limits', path), path)}, scopeFields: {compact_fields(_read(trace, 'scope_fields', path), path)}, "
            f"scopeLimits: {_limits_literal(_read(trace, 'scope_limits', path), path)}, allowedEvents: []familyEventContract{{{event_literals}}}, "
            f"eventLimits: {_limits_literal(_read(trace, 'event_limits', path), path)}, maxEvents: {_read(trace, 'max_events', path)}, "
            "allowedLinks: []string{" + ", ".join(_typed_symbol(item, path) for item in links) + "}, "
            f"linkFields: {compact_fields(_read(trace, 'link_fields', path), path)}, linkLimits: {_limits_literal(_read(trace, 'link_limits', path), path)}, maxLinks: {_read(trace, 'max_links', path)}, "
            f"scopeName: {_go_string(_read(trace, 'scope_name', path), path)}, scopeSchemaURL: {_go_string(_read(trace, 'scope_schema_url', path), path)}, "
            f"traceSchemaVersion: {_go_string(_read(trace, 'trace_schema_version', path), path)}, semanticProfile: {_go_string(_read(trace, 'semantic_profile', path), path)}}},"
        )
        for event_position, event in enumerate(events):
            event_path = f"{path}.events[{event_position}]"
            helper = _identifier(_read(event, "private_helper_symbol", event_path), event_path)
            literal = (
                "familyEventContract{"
                + ", ".join(
                    (
                        f"id: {_go_string(_read(event, 'event_id', event_path), event_path)}",
                        f"name: {_typed_symbol(_read(event, 'event_name', event_path), event_path)}",
                        f"fields: {_field_descriptor_slice(_read(event, 'fields', event_path), event_path)}",
                    )
                )
                + "}"
            )
            event_rows.append((helper, literal))
    lines.extend(
        (
            "}",
            "",
        )
    )
    for helper, literal in event_rows:
        lines.extend((f"func {helper}() familyEventContract {{", f"\treturn {literal}", "}", ""))
    for family_id, _, signal, _, symbol in identity_rows:
        if signal == "span":
            lines.extend((f"type {symbol} struct {{", "\tdynamicResourceFields []familyFieldDescriptor", "}", ""))
        else:
            lines.extend((f"type {symbol} struct{{}}", ""))
        lines.extend(
            (
                f"func ({symbol}) familyDescriptorContract() familyDescriptorContract {{",
                f"\treturn generatedFamilyBaseContracts[{_go_string(family_id, 'family descriptor')}]",
                "}",
                "",
            )
        )
        if signal == "metric":
            lines.extend(
                (
                    f"func ({symbol}) familyMetricContract() familyMetricContract {{",
                    f"\treturn generatedFamilyMetricContracts[{_go_string(family_id, 'metric descriptor')}]",
                    "}",
                    "",
                )
            )
        elif signal == "span":
            lines.extend(
                (
                    f"func (descriptor {symbol}) familyTraceContract() familyTraceContract {{",
                    f"\tcontract := generatedFamilyTraceContracts[{_go_string(family_id, 'trace descriptor')}]",
                    "\tcontract.resourceFields = append(append([]familyFieldDescriptor(nil), contract.resourceFields...), descriptor.dynamicResourceFields...)",
                    "\treturn contract",
                    "}",
                    "",
                )
            )
    lines.extend(
        (
            "type generatedFamilyIdentityDescriptor struct {",
            "\tFamilyID string",
            "\tIdentity EventIdentity",
            "\tDescriptor familyDescriptor",
            "}",
            "",
            f"func generatedFamilyIdentityDescriptors() [{len(descriptors)}]generatedFamilyIdentityDescriptor {{",
            f"\treturn [{len(descriptors)}]generatedFamilyIdentityDescriptor{{",
        )
    )
    for family_id, bucket, signal, name, symbol in identity_rows:
        lines.append(
            "\t\t{"
            + f"FamilyID: {_go_string(family_id, 'family identity table')}, "
            + f"Identity: EventIdentity{{Bucket: Bucket({_go_string(bucket, 'family identity table')}), Signal: {_FAMILY_SIGNAL_SYMBOLS[signal]}, Name: EventName({_go_string(name, 'family identity table')})}}, "
            + f"Descriptor: {symbol}{{}}"
            + "},"
        )
    lines.extend(("\t}", "}", ""))
    return _go_source(lines)


def _render_catalog_body(plan: Any) -> bytes:
    descriptors = _sequence(_read(plan, "descriptors", "GoAPIPlanIR"), "GoAPIPlanIR.descriptors", maximum=4096)
    if not descriptors:
        raise GoRenderError("GoAPIPlanIR.descriptors: family descriptor inventory is empty")
    family_ids: set[str] = set()
    identities: set[tuple[str, str, str]] = set()
    signal_name_owners: dict[tuple[str, str], str] = {}
    identity_rows: list[tuple[str, str, str, str, str]] = []
    for position, descriptor in enumerate(descriptors):
        path = f"GoAPIPlanIR.descriptors[{position}]"
        family_id = _string(_read(descriptor, "family_id", path), f"{path}.family_id")
        signal = _string(_read(descriptor, "signal", path), f"{path}.signal")
        bucket = _string(_read(descriptor, "identity_bucket", path), f"{path}.identity_bucket")
        name = _string(_read(descriptor, "identity_name", path), f"{path}.identity_name")
        catalog = _read(descriptor, "catalog_contract", path)
        symbol = _identifier(_read(catalog, "descriptor_type_symbol", path), f"{path}.descriptor_type_symbol")
        if signal not in _FAMILY_SIGNALS:
            raise GoRenderError(f"{path}.signal: unknown family signal {signal!r}")
        if family_id in family_ids:
            raise GoRenderError(f"{path}.family_id: duplicate family ID {family_id!r}")
        identity = (bucket, signal, name)
        if identity in identities:
            raise GoRenderError(f"{path}: duplicate family identity {identity!r}")
        signal_name = (signal, name)
        previous_bucket = signal_name_owners.get(signal_name)
        if previous_bucket is not None and previous_bucket != bucket:
            raise GoRenderError(f"{path}: family name {name!r} for signal {signal!r} has conflicting bucket owners")
        family_ids.add(family_id)
        identities.add(identity)
        signal_name_owners[signal_name] = bucket
        identity_rows.append((family_id, bucket, signal, name, symbol))

    return _render_compact_catalog_body(descriptors, identity_rows)


def _emit_value_bindings(bindings: Any, *, target: str, prefix: str = "generated") -> tuple[list[str], str]:
    lines: list[str] = []
    literals: list[str] = []
    for position, binding in enumerate(_sequence(bindings, "value bindings", maximum=4096)):
        path = f"value bindings[{position}]"
        key = _go_string(_read(binding, "key", path), f"{path}.key")
        selector = _identifier(_read(binding, "selector", path), f"{path}.selector")
        conversion = _read(binding, "conversion_op", path)
        local = f"{prefix}Value{position}"
        present = f"{prefix}Present{position}"
        if conversion == "required_scalar":
            literals.append(f"{{key: {key}, value: input.{selector}, present: true}}")
        elif conversion == "optional_scalar":
            lines.append(f"{local}, {present} := input.{selector}.Get()")
            literals.append(f"{{key: {key}, value: {local}, present: {present}}}")
        elif conversion == "copied_string_slice":
            if _read(binding, "presence", path) == "required":
                literals.append(f"{{key: {key}, value: append([]string(nil), input.{selector}...), present: true}}")
            else:
                lines.extend(
                    (
                        f"{local}, {present} := input.{selector}.Get()",
                        f"if {present} {{ {local} = append([]string(nil), {local}...) }}",
                    )
                )
                literals.append(f"{{key: {key}, value: {local}, present: {present}}}")
        elif conversion == "structured_encoder":
            encoder = _read(binding, "structured_encoder_symbol", path)
            if encoder is None:
                raise GoRenderError(f"{path}: structured binding has no encoder")
            encoder = _identifier(encoder, f"{path}.structured_encoder_symbol")
            if _read(binding, "presence", path) == "required":
                lines.append(f"{local}, err := {encoder}({key}, input.{selector}, true)")
            else:
                value = f"{prefix}Input{position}"
                lines.extend(
                    (
                        f"{value}, {present} := input.{selector}.Get()",
                        f"{local}, err := {encoder}({key}, {value}, {present})",
                    )
                )
            lines.append(f"if err != nil {{ return {target}{{}}, err }}")
            literals.append(local)
        else:
            raise GoRenderError(f"{path}: unsupported value conversion opcode {conversion}")
    return lines, "familyFieldValues{" + ", ".join(literals) + "}"


def _conditions_literal(bindings: Any) -> str:
    items: list[tuple[str, str, bool]] = []
    for position, binding in enumerate(_sequence(bindings, "condition bindings", maximum=4096)):
        path = f"condition bindings[{position}]"
        selector = _identifier(_read(binding, "selector", path), f"{path}.selector")
        optional_source = _read(binding, "optional_source", path)
        if type(optional_source) is not bool:
            raise GoRenderError(f"{path}.optional_source: expected boolean")
        items.append(
            (_go_string(_read(binding, "condition_id", path), f"{path}.condition_id"), selector, optional_source)
        )
    if not items:
        return "familyConditionFacts{}"
    lines = ["familyConditionFacts{"]
    for condition_id, selector, optional_source in items:
        predicate = (
            f"value, present := input.{selector}.Get(); present && value" if optional_source else f"input.{selector}"
        )
        lines.extend(
            (
                "\t\t\t{",
                f"\t\t\t\tid: {condition_id},",
                "\t\t\t\tstate: func() familyConditionState {",
                f"\t\t\t\t\tif {predicate} {{",
                "\t\t\t\t\t\treturn familyConditionTrue",
                "\t\t\t\t\t}",
                "\t\t\t\t\treturn familyConditionFalse",
                "\t\t\t\t}(),",
                "\t\t\t},",
            )
        )
    lines.append("\t\t}")
    return "\n".join(lines)


